use lithe::{
    fmc_core::Term,
    interpreter::Interpreter,
    machines::{
        KrivineMachine, Machine, StackMachine,
        machine::{MachineType, StepResult},
        runtime_io::parse_input_term,
    },
};
use std::fs;

fn compile_program(program: &str, rnd: bool) -> lithe::fmc_core::Term {
    Interpreter::new(true, MachineType::Stack)
        .compile(program.to_string(), false, rnd)
        .unwrap()
}

fn run_stack(program: &str) -> (StepResult, Vec<String>) {
    let term = compile_program(program, false);
    let mut machine = StackMachine::new(term);
    machine.set_silent(true);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn run_krivine(program: &str) -> (StepResult, Vec<String>) {
    let term = compile_program(program, false);
    let mut machine = KrivineMachine::new(term);
    machine.set_silent(true);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn run_stack_with_input(program: &str, inputs: Vec<Term>) -> (StepResult, Vec<String>) {
    let term = compile_program(program, false);
    let mut machine = StackMachine::new(term);
    machine.set_silent(true);
    machine.seed_input(inputs);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn run_krivine_with_input(program: &str, inputs: Vec<Term>) -> (StepResult, Vec<String>) {
    let term = compile_program(program, false);
    let mut machine = KrivineMachine::new(term);
    machine.set_silent(true);
    machine.seed_input(inputs);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn run_stack_with_rnd(program: &str) -> (StepResult, Vec<String>) {
    let term = compile_program(program, true);
    let mut machine = StackMachine::new(term);
    machine.set_silent(true);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn run_krivine_with_rnd(program: &str) -> (StepResult, Vec<String>) {
    let term = compile_program(program, true);
    let mut machine = KrivineMachine::new(term);
    machine.set_silent(true);
    let result = machine.run(false);
    (result, machine.output().to_vec())
}

fn assert_outputs_on_both_machines(program: &str, expected: &[&str]) {
    let (stack_result, stack_output) = run_stack_with_rnd(program);
    let (krivine_result, krivine_output) = run_krivine_with_rnd(program);

    assert_eq!(stack_result, StepResult::Stop);
    assert_eq!(krivine_result, StepResult::Stop);
    assert_eq!(stack_output, expected);
    assert_eq!(krivine_output, expected);
}

