#[test]
fn multi_parameter_functions_can_call_other_functions() {
    let program = r#"
        fn add(a, b) {
            return $a + $b;
        }

        fn combine(a, b, c) {
            return add(add($a, $b), $c);
        }

        print combine(1, 2, 3);
    "#;

    assert_outputs_on_both_machines(program, &["6"]);
}

