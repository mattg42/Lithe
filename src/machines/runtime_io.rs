mod tests {
    use super::{Number, number_to_term, parse_input_term, term_to_number};
    use crate::fmc_core::{Choice, Term, choice::Constant};

    #[test]
    fn parses_boolean_and_integer_input_terms() {
        assert_eq!(
            parse_input_term("true"),
            Some(Term::Choice(Choice::Constant(Constant::Boolean(true))))
        );
        assert_eq!(
            parse_input_term("false"),
            Some(Term::Choice(Choice::Constant(Constant::Boolean(false))))
        );
        assert_eq!(
            parse_input_term("42"),
            Some(Term::Choice(Choice::Constant(Constant::Integer(42))))
        );
    }

    #[test]
    fn trims_whitespace_and_rejects_invalid_input() {
        assert_eq!(
            parse_input_term("  -7 \n"),
            Some(Term::Choice(Choice::Constant(Constant::Integer(-7))))
        );
        assert_eq!(parse_input_term("hello"), None);
        assert_eq!(parse_input_term(""), None);
    }

    #[test]
    fn converts_between_numeric_terms_and_runtime_numbers() {
        assert_eq!(
            term_to_number(Term::Choice(Choice::Constant(Constant::Integer(3)))),
            Some(Number::Integer(3))
        );
        assert_eq!(
            term_to_number(Term::Choice(Choice::Constant(Constant::Float(1.5)))),
            Some(Number::Float(1.5))
        );
        assert_eq!(
            number_to_term(Number::Float(2.25)),
            Term::Choice(Choice::Constant(Constant::Float(2.25)))
        );
    }
}
